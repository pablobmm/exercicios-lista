package Lista_objetos_distintos.Ex6;

public class ContaBancaria {
    private int numero;
    private double saldo;

    public ContaBancaria(int numero,double saldo){
        this.numero = numero;
        this.saldo = saldo;
    }

    public int getNumero() {
        return numero;
    }

    public double getSaldo() {
        return saldo;
    }
}
